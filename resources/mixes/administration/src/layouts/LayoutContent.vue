<script>
  import Core from '../main'
  export default {
    computed: {
      message: {
        get () {
          return this.$store.state.message
        }
      }
    },
    methods: {
      close: function () {
        this.$store.commit('message', {
          show: false
        })
      }
    },
    mounted () {
      Core.instance.fixStyle()
    },
    watch: {
      message: {
        deep: true,
        handler: function (val, old) {
          let _this = this
          let _message = _this.$store.state.message
          if (val.show === true && old.show === false) {
            _this.$jquery('html, body').animate({
              scrollTop: 0
            }, 'slow')
            _message.type === 'notice' && setTimeout(function () {
              _this.$store.commit('message', {
                show: false
              })
            }, 2500)
          }
        }
      }
    }
  }
</script>
<template>
    <div class="content-wrapper">
        <section class="content">
            <div class="alert alert-dismissible" :class="'alert-' + message.type" v-show="message.show">
                <button type="button" class="close" @click="close">×</button>
                <div>
                    <h4>提示：</h4>
                    <p v-for="text in message.text">{{ text }}</p>
                </div>
            </div>
            <slot></slot>
        </section>
    </div>
</template>